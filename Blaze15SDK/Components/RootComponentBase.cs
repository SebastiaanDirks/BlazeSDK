public static class RootComponentUtils
{
    public static ushort WithErrorPrefix(this ServerError error)
    {
        ushort errorCode = (ushort)error;
        errorCode |= 0x4000;
        return errorCode;
    }

    public static ushort WithErrorPrefix(this SdkError error)
    {
        ushort errorCode = (ushort)error;
        errorCode |= 0x8000;
        return errorCode;
    }
}
